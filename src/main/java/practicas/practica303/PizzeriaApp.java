package practicas.practica303;

import practicas.practica303.dao.PizzeriaDao;
import practicas.practica303.dao.impl.PizzaDaoMemoria;
import practicas.practica303.service.PizzeriaService;

import java.util.Scanner;

public class PizzeriaApp {

    PizzeriaService pizzeriaService;
    PizzeriaDao pizzeriaDao;

    public static void main(String[] args) {
        PizzeriaApp pizzeriaApp = new PizzeriaApp();
        pizzeriaApp.cfg();
        pizzeriaApp.run();
    }

    private void cfg() {
        pizzeriaDao = new PizzaDaoMemoria();
        //pizzeriaDao = new PizzaDaoFichero(new File("pizzeria.txt"));
        pizzeriaService = new PizzeriaService(pizzeriaDao);
    }

    public void run() {
        Scanner scanner = new Scanner(System.in);
        int opcion;

        do {
            System.out.println("1. Agregar pizza");
            System.out.println("2. Mostrar pizzas");
            System.out.println("3. Eliminar pizza");
            System.out.println("4. Salir");
            System.out.print("Elige una opción: ");
            opcion = scanner.nextInt();
            scanner.nextLine(); // Limpiar el buffer

            switch (opcion) {
                case 1:
                    System.out.print("Nombre de la pizza: ");
                    String pizza = scanner.nextLine();
                    pizzeriaService.agregarPizza(pizza);
                    break;
                case 2:
                    System.out.println("Pizzas disponibles:");
                    for (String p : pizzeriaService.obtenerPizzas()) {
                        System.out.println(p);
                    }
                    break;
                case 3:
                    System.out.print("Nombre de la pizza a eliminar: ");
                    pizza = scanner.nextLine();
                    pizzeriaService.eliminarPizza(pizza);
                    break;
                case 4:
                    System.out.println("Saliendo...");
                    break;
                default:
                    System.out.println("Opción no válida");
            }
        } while (opcion != 4);

        scanner.close();
    }
}
